package com.beber.object;

public class Composition {
	private int id;
	private int id_produit;
	private int id_recette;
	private int quantite;
	private int id_unit;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the id_produit
	 */
	public int getId_produit() {
		return id_produit;
	}
	/**
	 * @param id_produit the id_produit to set
	 */
	public void setId_produit(int id_produit) {
		this.id_produit = id_produit;
	}
	/**
	 * @return the id_recette
	 */
	public int getId_recette() {
		return id_recette;
	}
	/**
	 * @param id_recette the id_recette to set
	 */
	public void setId_recette(int id_recette) {
		this.id_recette = id_recette;
	}
	/**
	 * @return the quantite
	 */
	public int getQuantite() {
		return quantite;
	}
	/**
	 * @param quantite the quantite to set
	 */
	public void setQuantite(int quantite) {
		this.quantite = quantite;
	}
	/**
	 * @return the id_unit
	 */
	public int getId_unit() {
		return id_unit;
	}
	/**
	 * @param id_unit the id_unit to set
	 */
	public void setId_unit(int id_unit) {
		this.id_unit = id_unit;
	}

}
